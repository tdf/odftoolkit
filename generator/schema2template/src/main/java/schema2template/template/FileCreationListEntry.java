package schema2template.template;

import java.util.HashMap;
import java.util.Map;

/**
 * Is created by FileCreationListHandler. Every file that is generated by a Velocity template is a
 * line in the file-creation-list. A future file is a single entry in the file-creation-list.
 *
 * @author Hans-Peter Schaal
 */
public class FileCreationListEntry {

  /** Type of file-creation-list entry (file or path) */
  public enum EntryType {
    FILE;
    // PATH;
  }

  private EntryType mType;
  private Map<String, String> mAttributes;
  private int mLineNumber;

  /** @param type Use FilelistEntry.EntryType.[FILE] */
  public FileCreationListEntry(EntryType type) {
    this(type, -1);
  }

  /**
   * @param type Use FilelistEntry.EntryType.[FILE]
   * @param lineNumber number in .xml for logging / error output.
   */
  public FileCreationListEntry(EntryType type, int lineNumber) {
    mType = type;
    mAttributes = new HashMap<String, String>();
    mLineNumber = lineNumber;
  }

  /** @return the entry type */
  public EntryType getType() {
    return mType;
  }

  /** @return line number in file-creation-list.xml for logging / error output. */
  public int getLineNumber() {
    return mLineNumber;
  }

  /**
   * @param key Attribute Key
   * @return Attribute Value
   */
  public String getAttribute(String key) {
    return mAttributes.get(key);
  }

  /** @return Attributes as map */
  public Map<String, String> getAttributes() {
    return mAttributes;
  }

  /**
   * @param key Attribute Key
   * @param value Attribute Value. If null, delete the key.
   */
  public void setAttribute(String key, String value) {
    if (key == null) {
      return;
    }
    if (value == null) {
      mAttributes.remove(key);
    } else {
      mAttributes.put(key, value);
    }
  }
}
